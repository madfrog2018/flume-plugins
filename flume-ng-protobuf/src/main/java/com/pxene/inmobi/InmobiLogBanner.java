public class InmobiLogBanner {

	private Integer w;
	private Integer h;
	private String id;
	private Integer[] btype;
	private Integer[] battr;
	public Integer getW() {
		return w;
	}
	public void setW(Integer w) {
		this.w = w;
	}
	public Integer getH() {
		return h;
	}
	public void setH(Integer h) {
		this.h = h;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Integer[] getBtype() {
		return btype;
	}
	public void setBtype(Integer[] btype) {
		this.btype = btype;
	}
	public Integer[] getBattr() {
		return battr;
	}
	public void setBattr(Integer[] battr) {
		this.battr = battr;
	}
	
}
